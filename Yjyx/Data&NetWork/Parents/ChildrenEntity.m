#import "ChildrenEntity.h"

@implementation ChildrenEntity
+(ChildrenEntity *)wrapChildrenWithDic:(NSDictionary *)dic{
    ChildrenEntity *children = [[ChildrenEntity alloc] init];
    children.childavatar = [dic objectForKey:@"childavatar"];
    children.cid = [dic objectForKey:@"cid"];
    children.name = [dic objectForKey:@"name"];
    children.relation = [dic objectForKey:@"relation"];
    return children;
}
@end
